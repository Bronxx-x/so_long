#include "ft_printf.h"

int	ft_putusnbr(unsigned int n)
{
	int	len;

	len = 0;
	if (n > 9)
		len = ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
	len++;
	return (len);
}
